/**
 * @file    rgb565_utils.h
 * @brief   RGB565 色彩格式的提取、打包和双线性插值辅助函数
 */
#ifndef RGB565_UTILS_H
#define RGB565_UTILS_H

#include <stdint.h>

/* ==================================================================== */
/*                   通道提取 / 打包宏                                    */
/* ==================================================================== */

/** 从 RGB565 中提取 R 通道（5bit，值域 0~31） */
#define RGB565_R(v)  (((v) >> 11) & 0x1F)

/** 从 RGB565 中提取 G 通道（6bit，值域 0~63） */
#define RGB565_G(v)  (((v) >> 5)  & 0x3F)

/** 从 RGB565 中提取 B 通道（5bit，值域 0~31） */
#define RGB565_B(v)  ((v)         & 0x1F)

/** 将 R(5bit)/G(6bit)/B(5bit) 打包为 RGB565 */
#define RGB565_PACK(r, g, b)  ((uint16_t)(((r) << 11) | ((g) << 5) | (b)))

/* ==================================================================== */
/*                   双线性插值函数                                       */
/* ==================================================================== */

/**
 * @brief  对2个RGB565像素按通道取平均（用于水平/垂直插值）
 * @param  p0, p1  输入像素
 * @return 平均后的像素
 */
uint16_t RGB565_Avg2(uint16_t p0, uint16_t p1);

/**
 * @brief  对4个RGB565像素按通道取平均（用于双线性插值）
 * @param  p0, p1, p2, p3  输入像素（通常为左上、右上、左下、右下）
 * @return 平均后的像素
 */
uint16_t RGB565_Avg4(uint16_t p0, uint16_t p1, uint16_t p2, uint16_t p3);

#endif /* RGB565_UTILS_H */
